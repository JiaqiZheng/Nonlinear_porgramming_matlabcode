A=[2,1,3;8,1,2;7,2,4;-1,0,0;0,-1,0;0,0,-1];
b=[2;,1;7;0;0;0];
k=200;
X=zeros(3,k);
alpha=0.01;
X(:,1)=[2;5;3];
for i=2:k
    f=[X(1,i-1)-3, X(2,i-1)-4, X(3,i-1)-5];
    x=linprog(f,A,b);
    X(:,i)=X(:,i-1)+alpha*(x-X(:,i-1));
end

plot3(X(1,1:5),X(2,1:5),X(3,1:5),'-*')
xlabel('x')
ylabel('y')
zlabel('z')